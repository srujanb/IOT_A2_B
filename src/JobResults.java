public class JobResults {

    float t;
    float d;
    float t95;
    float d95;
    float p;
    float forS;


    public float getT() {
        return t;
    }

    public void setT(float t) {
        this.t = t;
    }

    public float getD() {
        return d;
    }

    public void setD(float d) {
        this.d = d;
    }

    public float getT95() {
        return t95;
    }

    public void setT95(float t95) {
        this.t95 = t95;
    }

    public float getD95() {
        return d95;
    }

    public void setD95(float d95) {
        this.d95 = d95;
    }

    public float getP() {
        return p;
    }

    public void setP(float p) {
        this.p = p;
    }

    public float getForS() {
        return forS;
    }

    public void setForS(float forS) {
        this.forS = forS;
    }
}
